USE TaxiTripStaging_Local;
CREATE TABLE RawTripData (
    OBJECTID INT,
    TRIPTYPE VARCHAR(50),
    PROVIDERNAME VARCHAR(100),
    FAREAMOUNT DECIMAL(10,2),
    GRATUITYAMOUNT DECIMAL(10,2),
    SURCHARGEAMOUNT DECIMAL(10,2),
    EXTRAAREAMOUNT DECIMAL(10,2),
    TOLLAMOUNT DECIMAL(10,2),
    TOTALAMOUNT DECIMAL(10,2),
    PAYMENTTYPE VARCHAR(50),
    ORIGINCITY VARCHAR(50),
    ORIGINSTATE VARCHAR(50),
    ORIGINZIP VARCHAR(10),
    DESTINATIONCITY VARCHAR(50),
    DESTINATIONSTATE VARCHAR(50),
    DESTINATIONZIP VARCHAR(10),
    MILEAGE DECIMAL(8,2),
    DURATION INT,
    ORIGIN_BLOCK_LATITUDE DECIMAL(9,6),
    ORIGIN_BLOCK_LONGITUDE DECIMAL(9,6),
    ORIGIN_BLOCKNAME VARCHAR(100),
    DESTINATION_BLOCK_LAT DECIMAL(9,6),
    DESTINATION_BLOCK_LONG DECIMAL(9,6),
    DESTINATION_BLOCKNAME VARCHAR(100),
    AIRPORT VARCHAR(1), -- 'Y' or 'N'
    ORIGINDATETIME_TR DATETIME,
    DESTINATIONDATETIME_TR DATETIME,
    Month VARCHAR(7) -- e.g., '2024_01'
);